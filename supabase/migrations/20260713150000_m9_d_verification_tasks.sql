CREATE OR REPLACE FUNCTION lifecycle.generate_verification_tasks()
RETURNS TABLE (tasks_created INT, tasks_skipped INT, detail TEXT)
LANGUAGE plpgsql AS $$
DECLARE v_created INT := 0; v_skipped INT := 0; r RECORD;
BEGIN
  -- 1. ownership_period.effective_from = NULL (priority: HIGH)
  FOR r IN
    SELECT op.id AS op_id, op.property_name, ei.canonical_name AS partner_name
    FROM lifecycle.ownership_period op
    JOIN lifecycle.partner_entry pe ON pe.property_name = op.property_name AND pe.entity_id = op.entity_id
    JOIN lifecycle.entity_identity ei ON ei.id = pe.entity_id
    WHERE op.effective_from IS NULL AND op.effective_from_confidence = 'pending_verification' AND op.status != 'void'
  LOOP
    INSERT INTO lifecycle.verification_tasks (source_table,source_id,missing_field,reason,expected_source_type,search_strategy,priority)
    VALUES ('ownership_period',r.op_id,'effective_from','missing_date','partnership_agreement',
      jsonb_build_array(
        jsonb_build_object('order',1,'source','financial_documents','match','partnership agreement '||r.property_name),
        jsonb_build_object('order',2,'source','email','match',r.partner_name||' '||r.property_name),
        jsonb_build_object('order',3,'source','whatsapp','match',r.partner_name||' partnership'),
        jsonb_build_object('order',4,'source','google_drive','match',r.property_name||' agreement'),
        jsonb_build_object('order',5,'source','manual_upload','match',NULL)),'high')
    ON CONFLICT (source_id,missing_field,reason) DO NOTHING;
    IF FOUND THEN v_created := v_created+1; ELSE v_skipped := v_skipped+1; END IF;
  END LOOP;
  -- 2. capital_event.effective_date = NULL (priority: MEDIUM)
  FOR r IN
    SELECT ce.id AS ce_id, ce.property_name, ce.amount_eur, ei.canonical_name AS payer_name
    FROM lifecycle.capital_event ce
    JOIN lifecycle.entity_identity ei ON ei.id = ce.entity_id
    WHERE ce.effective_date IS NULL AND ce.effective_date_confidence = 'pending_verification' AND ce.status != 'void'
  LOOP
    INSERT INTO lifecycle.verification_tasks (source_table,source_id,missing_field,reason,expected_source_type,search_strategy,priority)
    VALUES ('capital_event',r.ce_id,'effective_date','missing_date','bank_transfer',
      jsonb_build_array(
        jsonb_build_object('order',1,'source','financial_documents','match','bank transfer '||r.payer_name||' '||r.property_name),
        jsonb_build_object('order',2,'source','email','match',r.payer_name||' transfer '||r.amount_eur::TEXT),
        jsonb_build_object('order',3,'source','whatsapp','match',r.payer_name||' '||r.amount_eur::TEXT),
        jsonb_build_object('order',4,'source','manual_upload','match',NULL)),'medium')
    ON CONFLICT (source_id,missing_field,reason) DO NOTHING;
    IF FOUND THEN v_created := v_created+1; ELSE v_skipped := v_skipped+1; END IF;
  END LOOP;
  -- 3. partner_entry with zero capital_event rows (Oren pattern, priority: HIGH)
  FOR r IN
    SELECT pe.id AS pe_id, pe.property_name, ei.canonical_name AS partner_name
    FROM lifecycle.partner_entry pe
    JOIN lifecycle.entity_identity ei ON ei.id = pe.entity_id
    WHERE pe.status != 'void'
      AND NOT EXISTS (SELECT 1 FROM lifecycle.capital_event ce WHERE ce.entity_id=pe.entity_id AND ce.property_name=pe.property_name AND ce.event_subtype='partner_entry_payment' AND ce.status!='void')
  LOOP
    INSERT INTO lifecycle.verification_tasks (source_table,source_id,missing_field,reason,expected_source_type,search_strategy,priority)
    VALUES ('partner_entry',r.pe_id,'capital_payment','unknown_partner_payment','bank_transfer',
      jsonb_build_array(
        jsonb_build_object('order',1,'source','financial_documents','match','bank transfer '||r.partner_name||' '||r.property_name),
        jsonb_build_object('order',2,'source','email','match',r.partner_name||' payment '||r.property_name),
        jsonb_build_object('order',3,'source','whatsapp','match',r.partner_name||' paid'),
        jsonb_build_object('order',4,'source','manual_upload','match',NULL)),'high')
    ON CONFLICT (source_id,missing_field,reason) DO NOTHING;
    IF FOUND THEN v_created := v_created+1; ELSE v_skipped := v_skipped+1; END IF;
  END LOOP;
  RETURN QUERY SELECT v_created, v_skipped,
    'Scan complete. Tasks created: '||v_created||'. Tasks already existed (skipped): '||v_skipped||'.' AS detail;
END;
$$;

COMMENT ON FUNCTION lifecycle.generate_verification_tasks() IS
  'On-demand scanner: creates verification_tasks for all pending lifecycle facts. '
  'Idempotent. Covers: (1) ownership_period.effective_from=NULL, '
  '(2) capital_event.effective_date=NULL, (3) partner_entry with no capital_events. '
  'Usage: SELECT * FROM lifecycle.generate_verification_tasks();';
