        jsonb_build_object('order', 4, 'source', 'manual_upload', 'match', NULL)
      ),
      'medium'
    )
    ON CONFLICT (source_id, missing_field, reason) DO NOTHING;
    IF FOUND THEN v_created := v_created + 1;
    ELSE          v_skipped := v_skipped + 1;
    END IF;
  END LOOP;

